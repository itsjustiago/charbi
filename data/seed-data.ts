/**
 * Dataset do Charbi.
 *
 * Cronologia segundo a cronologia bíblica publicada no jw.org, derivada das
 * idades registadas em Génesis 5 e 11 e dos pontos de ancoragem históricos
 * (criação de Adão em 4026 a.E.C., Dilúvio em 2370 a.E.C., nascimento de
 * Abraão em 2018 a.E.C., Êxodo em 1513 a.E.C., destruição de Jerusalém em
 * 607 a.E.C., nascimento de Jesus em 2 a.E.C., morte de Jesus em 33 E.C.).
 *
 * Anos: inteiros com sinal. Negativo = a.E.C. ; positivo = E.C. ; não há ano 0.
 * Datas marcadas com `dateApprox: true` são estimativas (a Bíblia não regista
 * o ano exato); servem para posicionar a figura na linha do tempo.
 */

export type EraSeed = {
  slug: string;
  name: string;
  startYear: number;
  endYear: number;
  color: string;
  ordinal: number;
  summary: string;
};

export type CharacterSeed = {
  slug: string;
  name: string;
  nameEn: string;
  aka?: string[];
  gender: "m" | "f";
  era: string;
  birthYear?: number | null;
  deathYear?: number | null;
  lifespan?: number | null;
  dateApprox?: boolean;
  role: string;
  tagline: string;
  summary: string;
  keyFacts: string[];
  scriptures: string[];
  /** Termo alternativo para a pesquisa no jw.org (quando o nome é ambíguo). */
  jwQuery?: string;
};

export type RelationshipType =
  | "parent"
  | "spouse"
  | "sibling"
  | "ancestor"
  | "adoptive"
  | "relative";

export type RelationshipSeed = {
  from: string;
  to: string;
  type: RelationshipType;
};

